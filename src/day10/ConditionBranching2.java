package day10;

public class ConditionBranching2 {
	public static void main(String[] args) {
		
		if (5 > 3) {
			System.out.println("Yes");
		}
		
		if ('c' == 'r') {
			System.out.println("Yes, chars equal");
		}
		
		if (7 != 7) {
			System.out.println("Yes");
		} else {
			System.out.println("No");
		}
	}
}
